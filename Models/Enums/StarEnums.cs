namespace Echoes.API.Models.Enums
{
    public enum StarClass
    {
        O,           // Голубые гиганты (очень горячие, яркие, массивные)
        B,           // Бело-голубые
        A,           // Белые
        F,           // Желто-белые
        G,           // Желтые (как Солнце)
        K,           // Оранжевые
        M,           // Красные карлики (самые распространенные)
        L,           // Коричневые карлики
        T,           // Коричневые карлики (более холодные)
        Y,           // Коричневые карлики (самые холодные)
        WhiteDwarf,  // Белые карлики
        NeutronStar, // Нейтронные звезды
        BlackHole,   // Черные дыры
        RedGiant,    // Красные гиганты
        BlueGiant,   // Голубые гиганты
        WolfRayet,   // Вольф-Райе
        CarbonStar,  // Углеродные звезды
        Pulsar,      // Пульсары
        Binary,      // Двойные звезды
        Variable     // Переменные звезды
    }

    public enum StarLuminosityClass
    {
        Ia,     // Сверхгиганты высокой светимости
        Ib,     // Сверхгиганты низкой светимости
        II,     // Яркие гиганты
        III,    // Гиганты
        IV,     // Субгиганты
        V,      // Карлики главной последовательности
        VI,     // Субкарлики
        VII     // Белые карлики
    }

    public enum StarTemperature
    {
        UltraHot = 0,      // > 30,000K
        VeryHot = 1,       // 10,000 - 30,000K
        Hot = 2,           // 7,500 - 10,000K
        Warm = 3,          // 6,000 - 7,500K
        Moderate = 4,      // 5,200 - 6,000K (Солнце)
        Cool = 5,          // 3,700 - 5,200K
        Cold = 6,          // 2,400 - 3,700K
        VeryCold = 7,      // 1,300 - 2,400K
        UltraCold = 8      // < 1,300K
    }
}